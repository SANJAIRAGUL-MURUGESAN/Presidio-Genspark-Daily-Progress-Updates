#Example1 (number - positive, negative)
age = 25
quantity = -10
print(age,quantity)

#Example2 (float with decimal points)
pi = 3.14159
temperature = 27.5
print(pi, temperature)

#Example3 (String)
greet = "Hello World!"
print(greet)

#Example4 (Boolean)
is_valid = True
is_finished = False
print(is_valid,is_finished)

#Example5 (List)
fruits = ["apple", "banana", "cherry"]
numbers = [1, 5, 7, 3.2]
print(fruits, numbers)

#Example6 (tuple)
coordinates = (10, 20)
months = ("Jan", "Feb", "Mar")
print(coordinates,months)

#Example7 (set)
colors = {"red", "green", "blue"}
letters = set("python")  # removes duplicates
print(letters)

#Example8 (dictionary)
person = {"name": "Bob", "age": 30, "city": "New York"}
car_details = {"model": "Civic", "year": 2023, "color": "blue"}
print(person,car_details)

